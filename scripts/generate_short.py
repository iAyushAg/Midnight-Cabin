def escape_text(text):
    return (text
        .replace("\\", "\\\\")
        .replace("'", "\\'")
        .replace(":", "\\:")
        .replace("%", "\\%")
        .replace("\n", "\\n"))

def wrap_text(text, width=24):
    words = text.split()
    lines, current, count = [], [], 0
    for word in words:
        if count + len(word) > width:
            lines.append(" ".join(current))
            current, count = [word], len(word)
        else:
            current.append(word)
            count += len(word) + 1
    if current:
        lines.append(" ".join(current))
    return "\\n".join(lines)

